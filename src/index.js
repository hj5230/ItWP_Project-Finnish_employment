const mapFeatures = (feature, layer) => {
    let properties = feature.properties;
    layer.bindPopup(
        `
		<h4>${properties.name}</h4>
		<p>Employment 2020: ${properties.employ}</p>
		<p>Employment 2019: ${properties.prev}</p>
		`
    );
    layer.bindTooltip(properties.name);
}

const mapStyles = (feature) => {
	let hue;
    let percent = feature.properties.employ / feature.properties.prev;
	if(percent >= 1.01) return {color: 'hsl(120 75% 50%)'}
	else if(percent <= 0.97) return {color: 'hsl(10 75% 50%)'}
	else {
		if(percent <= 1) {
			hue = 2666.67 * percent - 2576.67;
		} else {
			hue = 3000 * percent - 2910;
		}
		return {color: 'hsl(' + hue + " 75% 50%)"}
	}
}

async function setChart(post) {
    const url = "https://statfin.stat.fi:443/PxWeb/api/v1/en/StatFin/tyokay/statfin_tyokay_pxt_115b.px";
    const promise = await fetch(url, {
        method: 'POST',
        headers: {"content-type": "application/json"},
        body: JSON.stringify(post),
    });
    if(!promise.ok) {
        return alert('Incorrect Input!');
    }
    const data = await promise.json();
    let value = data.value;
    let year = Object.values(data.dimension.Vuosi.category.label);
    let employ = [{'values': []}];
    for(let i = 0, len = value.length; i < len; ++i) {
        employ[0]['values'].push(value[i]);
    }
    let chartData = {
        labels: year,
        datasets: employ,
    }
    let chart = new frappe.Chart("#chart", {
        title: getTitle(),
        data: chartData,
        height: 250,
        type: "line",
        colors: ['#FF4500'],
    });
	// drag flush
	let drags = document.getElementsByTagName('span');
	let area = document.getElementById('chart');
	var drag = null;
	for(let i = 0, len = drags.length; i < len; ++i) {
		drags[i].ondragstart = () => {
			drag = drags[i];
		}
	}
	area.ondragover = (ev) => {
		ev.preventDefault();
	}
	area.ondrop = () => {
		flushChartByDrag(drag.id);
	}
	// input flush
	let input = document.getElementById('type');
	let submit = document.getElementById('find');
	submit.onclick = () => {
		flushChartByInput(input.value);
		return false;
	}
	// download image
	let download = document.getElementById('download');
	download.onclick = () => {
		html2canvas(document.getElementById('chart')).then(canvas => {
			let achr = document.createElement('a');
			achr.appendChild(canvas);
			achr.download = "data.png";
			achr.href = canvas.toDataURL();
			achr.target = "_blank";
			achr.click();
		});
	}
}

const getTitle = () => {
	let region = postJson.query[0].selection.values[0];
	let gender = postJson.query[2].selection.values[0];
	let age = postJson.query[3].selection.values[0];
	region = muniCode.variables[0].valueTexts[muniCode.variables[0].values.indexOf(region)];
	if(gender == 'SSS') {
		gender = "all genders";
	} else if(gender == '1') {
		gender = "male";
	} else if(gender == '2') {
		gender = "female";
	}
	if(age == 'SSS') {
		age = "all ages";
	}
	return "Employment data of " + region + ", " + gender + ", " + age + ".";
}

const flushChartByDrag = (label) => {
	if(label == 'gender-all') {
		postJson.query[2].selection.values = ['SSS'];
	} else if(label == 'gender-male') {
		postJson.query[2].selection.values = ['1'];
	} else if(label == 'gender-female') {
		postJson.query[2].selection.values = ['2'];
	} else if(label == 'age-all') {
		postJson.query[3].selection.values = ['SSS'];
	} else if(label == 'age-under') {
		postJson.query[3].selection.values = ['0-17'];
	} else if(label == 'age-adult') {
		postJson.query[3].selection.values = ['18-64'];
	} else if(label == 'age-over') {
		postJson.query[3].selection.values = ['65-'];
	}
	setChart(postJson);
}

const flushChartByInput = (input) => {
    let names = muniCode.variables[0].valueTexts;
    input = input[0].toUpperCase() + input.substring(1).toLowerCase();
    let target = muniCode.variables[0].values[names.indexOf(input)];
	postJson.query[0].selection.values = [target];
	setChart(postJson);
}

